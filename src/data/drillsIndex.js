// ── API publique ───────────────────────────────────────────────────────────
export const DRILL_IDS = Object.keys(DRILL_PATHS);

/** Charge un drill par son id (retourne une Promise) */
export const loadDrillById = (id) => {
  const loader = DRILL_PATHS[id];
  if (!loader) return Promise.resolve(null);
  return loader().then((m) => m.default ?? m);
};

/** Charge plusieurs drills en parallèle */
export const loadDrillsByIds = (ids) =>
  Promise.all(ids.map(loadDrillById)).then((res) => res.filter(Boolean));

/** Charge tous les drills d'une partie */
export const loadDrillsByPart = (part) => {
  const ids = DRILL_IDS.filter((id) => id.startsWith(`p${part}-`));
  return loadDrillsByIds(ids);
};
